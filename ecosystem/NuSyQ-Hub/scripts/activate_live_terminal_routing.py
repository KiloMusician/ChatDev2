def _to_windows_path(path: Path) -> str:
    path_str = str(path)
    if path_str.startswith("/mnt/") and len(path_str) > 6:
        drive = path_str[5].upper()
        tail = path_str[7:].replace("/", "\\")
        return f"{drive}:\\{tail}"
    return path_str


def generate_terminal_watcher_ps1(terminal_name: str, log_file: Path, output_dir: Path) -> Path:
    """Generate PowerShell script that watches a terminal log file."""
    log_file_name = log_file.name
    fallback_aliases = LOG_FILE_ALIASES.get(terminal_name, [])
    if fallback_aliases:
        fallback_literal = ",\n    ".join(f'(Join-Path $logDir "{alias_name}")' for alias_name in fallback_aliases)
        fallback_block = f"@(\n    {fallback_literal}\n)"
    else:
        fallback_block = "@()"
    ps1_content = f"""# Terminal Watcher for {terminal_name}
# Auto-generated by activate_live_terminal_routing.py

$repoRoot = Split-Path (Split-Path $PSScriptRoot -Parent) -Parent
$logDir = Join-Path $repoRoot "data\\terminal_logs"
$primaryLogFile = Join-Path $logDir "{log_file_name}"
$terminalName = "{terminal_name}"
$sourceLog = $primaryLogFile
$sourceLabel = "NuSyQ terminal log"
$fallbackLogs = {fallback_block}

if ($terminalName -eq "copilot") {{
    $logsRoot = Join-Path $env:APPDATA "Code\\logs"
    if (Test-Path $logsRoot) {{
        $copilotCandidates = Get-ChildItem -Path $logsRoot -Filter "GitHub Copilot Chat.log" -Recurse -File -ErrorAction SilentlyContinue |
            Sort-Object LastWriteTime -Descending
        if ($copilotCandidates) {{
            $sourceLog = $copilotCandidates[0].FullName
            $sourceLabel = "VS Code Copilot Chat log"
        }}
    }}
}}

if (($sourceLog -eq $primaryLogFile) -and (!(Test-Path $sourceLog))) {{
    foreach ($fallback in $fallbackLogs) {{
        if (![string]::IsNullOrWhiteSpace($fallback) -and (Test-Path $fallback)) {{
            $sourceLog = $fallback
            $sourceLabel = "NuSyQ legacy terminal log"
            break
        }}
    }}
}}

Write-Host "=== $terminalName Terminal - Live Output ===" -ForegroundColor Cyan
Write-Host "Source: $sourceLabel" -ForegroundColor Gray
Write-Host "Watching: $sourceLog" -ForegroundColor Gray
if ($sourceLog -ne $primaryLogFile) {{
    Write-Host "Mirror destination: $primaryLogFile" -ForegroundColor DarkGray
}}
Write-Host ""

# Ensure destination log exists
if (!(Test-Path $primaryLogFile)) {{
    New-Item -ItemType File -Path $primaryLogFile -Force | Out-Null
    Write-Host "Created log file: $primaryLogFile" -ForegroundColor Yellow
}}

if (!(Test-Path $sourceLog)) {{
    Write-Host "Source log not found: $sourceLog" -ForegroundColor Red
    Write-Host "Tip: open VS Code Copilot chat once, then relaunch this watcher." -ForegroundColor Yellow
    exit 1
}}

# Tail the log file and format output
Get-Content $sourceLog -Wait -Tail 20 | ForEach-Object {{
    $line = $_
    if ($null -eq $line) {{
        return
    }}

    $trimmed = $line.Trim()
    if ($trimmed.Length -eq 0) {{
        return
    }}

    $entry = $null
    if ($trimmed.StartsWith("{{") -and $trimmed.EndsWith("}}")) {{
        try {{
            $entry = $trimmed | ConvertFrom-Json -ErrorAction Stop
        }} catch {{
            $entry = $null
        }}
    }}

        if ($null -ne $entry) {{
        $timestamp = $entry.timestamp
        $level = $entry.level
        $message = $entry.message
        if ($null -ne $message) {{
            $message = "$message"
            $message = ($message -replace '[^\\x00-\\x7F]', '?')
        }}

        # Color based on level
        $color = switch ($level) {{
            "ERROR" {{ "Red" }}
            "WARNING" {{ "Yellow" }}
            "INFO" {{ "White" }}
            "DEBUG" {{ "Gray" }}
            default {{ "White" }}
        }}

        Write-Host "[$timestamp] " -NoNewline -ForegroundColor Gray
        Write-Host "[$level] " -NoNewline -ForegroundColor $color
        Write-Host $message -ForegroundColor $color

        if ($sourceLog -ne $primaryLogFile) {{
            Add-Content -Path $primaryLogFile -Value $trimmed -Encoding UTF8 -ErrorAction SilentlyContinue
        }}
    }} else {{
        # If not JSON, print as plain text but mirror as NDJSON for tooling.
        $safeLine = ($line -replace '[^\\x00-\\x7F]', '?')
        Write-Host $safeLine -ForegroundColor White
        if ($sourceLog -ne $primaryLogFile) {{
            $mirrorEntry = @{{
                timestamp = (Get-Date).ToString("o")
                level = "INFO"
                source = $terminalName
                message = $safeLine
            }} | ConvertTo-Json -Compress
            Add-Content -Path $primaryLogFile -Value $mirrorEntry -Encoding UTF8 -ErrorAction SilentlyContinue
        }}
    }}
}}
"""

    ps1_file = output_dir / f"watch_{terminal_name}_terminal.ps1"
    ps1_file.write_text(ps1_content, encoding="utf-8")
    return ps1_file


def load_terminal_names(root: Path) -> list[str]:
    """Load terminal names from data/terminal_routing.json when available."""
    configured_names: list[str] = []
    config_path = root / "data" / "terminal_routing.json"
    if config_path.exists():
        try:
            data = json.loads(config_path.read_text(encoding="utf-8"))
            terminals = data.get("terminals", {})
            if isinstance(terminals, dict) and terminals:
                configured_names = list(terminals.keys())
        except (OSError, json.JSONDecodeError):
            pass

    merged: list[str] = []
    for terminal_name in configured_names + CANONICAL_TERMINAL_NAMES:
        if terminal_name not in merged:
            merged.append(terminal_name)
    return merged


def _terminal_label(terminal_name: str) -> str:
    return TERMINAL_LABEL_OVERRIDES.get(terminal_name, terminal_name.replace("_", " ").title())


def generate_vscode_tasks(terminal_names: list[str], output_dir: Path) -> dict:
    """Generate VSCode tasks.json for terminal watchers."""
    tasks = {
        "version": "2.0.0",
        "tasks": [],
    }

    # Create a task for each terminal watcher
    for terminal_name in terminal_names:
        ps1_file = output_dir / f"watch_{terminal_name}_terminal.ps1"
        ps1_cmd_path = _to_windows_path(ps1_file)
        label = _terminal_label(terminal_name)
        task = {
            "label": f"Watch {label} Terminal",
            "type": "shell",
            "command": f'powershell -NoProfile -ExecutionPolicy Bypass -NoExit -File "{ps1_cmd_path}"',
            "presentation": {
                "echo": True,
                "reveal": "always",
                "focus": False,
                "panel": "dedicated",
                "showReuseMessage": False,
                "clear": False,
            },
            "problemMatcher": [],
            "isBackground": True,
        }
        tasks["tasks"].append(task)

    agent_task_labels = [
        f"Watch {_terminal_label(name)} Terminal" for name in terminal_names if name in AGENT_TERMINAL_NAMES
    ]
    operational_task_labels = [
        f"Watch {_terminal_label(name)} Terminal" for name in terminal_names if name not in AGENT_TERMINAL_NAMES
    ]

    # Add master task to launch all watchers
    tasks["tasks"].append(
        {
            "label": "Watch All Agent Terminals",
            "dependsOn": agent_task_labels,
            "dependsOrder": "parallel",
            "problemMatcher": [],
        }
    )

    tasks["tasks"].append(
        {
            "label": "Watch All Operational Terminals",
            "dependsOn": operational_task_labels,
            "dependsOrder": "parallel",
            "problemMatcher": [],
        }
    )

    tasks["tasks"].append(
        {
            "label": "Watch ALL Terminals (Full System)",
            "dependsOn": [f"Watch {_terminal_label(t)} Terminal" for t in terminal_names],
            "dependsOrder": "parallel",
            "problemMatcher": [],
        }
    )

    return tasks


