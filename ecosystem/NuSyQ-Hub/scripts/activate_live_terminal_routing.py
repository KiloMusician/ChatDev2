def validate_terminal_routing(root: Path) -> int:
    """Validate terminal routing configuration and generated artifacts."""
    terminal_names = load_terminal_names(root)
    issues: list[str] = []
    warnings: list[str] = []

    routing_path = root / "data" / "terminal_routing.json"
    if not routing_path.exists():
        issues.append("Missing data/terminal_routing.json")

    log_dir = root / "data" / "terminal_logs"
    watcher_dir = root / "data" / "terminal_watchers"
    tasks_path = root / ".vscode" / "terminal_watcher_tasks.json"

    if not log_dir.exists():
        issues.append("Missing data/terminal_logs directory")
    if not watcher_dir.exists():
        issues.append("Missing data/terminal_watchers directory")

    for terminal_name in terminal_names:
        log_file = log_dir / f"{terminal_name}.log"
        watcher_file = watcher_dir / f"watch_{terminal_name}_terminal.ps1"
        if not log_file.exists():
            alias_paths = [log_dir / alias_name for alias_name in LOG_FILE_ALIASES.get(terminal_name, [])]
            if not any(path.exists() for path in alias_paths):
                warnings.append(f"Missing log file (created on first write): {log_file}")
        if not watcher_file.exists():
            issues.append(f"Missing watcher script: {watcher_file}")
        else:
            content = watcher_file.read_text(encoding="utf-8")
            normalized = content.replace("/", "\\")
            expected_names = [f"{terminal_name}.log", *LOG_FILE_ALIASES.get(terminal_name, [])]
            if "terminal_logs" not in normalized:
                issues.append(f"Watcher missing terminal_logs reference: {watcher_file}")
            elif not any(f'"{name}"' in content for name in expected_names):
                issues.append(f"Watcher log path mismatch for {terminal_name}: {watcher_file}")

    if tasks_path.exists():
        try:
            tasks = json.loads(tasks_path.read_text(encoding="utf-8"))
            task_labels = {task.get("label", "") for task in tasks.get("tasks", []) if isinstance(task, dict)}
            for terminal_name in terminal_names:
                expected_label = f"Watch {_terminal_label(terminal_name)} Terminal"
                if expected_label not in task_labels:
                    issues.append(f"Missing task: {expected_label}")
        except (OSError, json.JSONDecodeError):
            issues.append("Failed to parse .vscode/terminal_watcher_tasks.json")
    else:
        issues.append("Missing .vscode/terminal_watcher_tasks.json")

    if warnings:
        print("⚠️  Terminal routing validation warnings:")
        for warning in warnings:
            print(f"   - {warning}")

    if issues:
        print("❌ Terminal routing validation failed:")
        for issue in issues:
            print(f"   - {issue}")
        return 1

    print("✅ Terminal routing validation passed.")
    return 0


