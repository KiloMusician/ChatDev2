"""
app/game_engine/crafting_engine.py — R4 Crafting System
=========================================================
Combine raw components to create custom exploits, consumables, and tools.
Components are found by exploring nodes, dropped by dungeon enemies, or
purchased at the bank/market.

State is stored in gs.flags:
    "components"  : { component_id: quantity }
    "inventory"   : { item_id: quantity }      (crafted/consumable items)
    "active_buffs": [ { id, effect, remaining_commands } ]

Wire format compatible: all public methods return List[dict] with t/s keys.
"""
