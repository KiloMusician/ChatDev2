def get_dungeon_drop(enemy_level: int) -> Optional[str]:
    """Return a random component id based on enemy level, or None for no drop."""
    if random.random() < 0.35:  # 35% drop chance
        return None
    for (lo, hi), table in _DROP_TABLE_BY_LEVEL.items():
        if lo <= enemy_level <= hi:
            components, weights = zip(*table)
            return random.choices(components, weights=list(weights), k=1)[0]
    # Fallback: high-level drops
    high = list(_DROP_TABLE_BY_LEVEL[(13, 20)])
    components, weights = zip(*high)
    return random.choices(components, weights=list(weights), k=1)[0]


