#!/usr/bin/env python3
"""
agents/documenter.py — Documentation Generator Agent

Generates man pages for game commands, lore pages for agents/NPCs,
and developer documentation for major systems. Uses LLM when available,
falls back to deterministic templates otherwise.

Usage:
    python3 agents/documenter.py                     # document everything new
    python3 agents/documenter.py --man <cmd>         # generate one man page
    python3 agents/documenter.py --lore <agent_id>   # generate one lore page
    python3 agents/documenter.py --audit             # list missing docs
    python3 agents/documenter.py --dev               # generate dev system docs
    python3 agents/documenter.py --all               # regenerate all
    python3 agents/documenter.py --task <id>         # run for orchestrator task
"""
from __future__ import annotations

import argparse
import json
import os
import sys
import time
import urllib.request
from pathlib import Path
from typing import Optional

BASE_DIR = Path(__file__).parent.parent
DOCS_DIR = BASE_DIR / "docs"
MAN_DIR = DOCS_DIR / "commands"
LORE_DIR = DOCS_DIR / "lore"
DEV_DIR = DOCS_DIR / "development"
BASE_URL = "http://localhost:8008"

for d in (MAN_DIR, LORE_DIR, DEV_DIR):
    d.mkdir(parents=True, exist_ok=True)


def log(level: str, msg: str, **ctx):
    ts = time.strftime("%H:%M:%S")
    colors = {"INFO": "\033[36m", "OK": "\033[32m", "WARN": "\033[33m",
              "ERROR": "\033[31m", "GEN": "\033[35m"}
    c = colors.get(level, "\033[0m")
    r = "\033[0m"
    kv = "  ".join(f"{k}={v}" for k, v in ctx.items())
    print(f"{c}[{level}]{r} {ts} {msg}" + (f"  | {kv}" if kv else ""))


def _post(path: str, data: dict) -> dict:
    try:
        req = urllib.request.Request(
            BASE_URL + path, json.dumps(data).encode(),
            headers={"Content-Type": "application/json"}, method="POST",
        )
        with urllib.request.urlopen(req, timeout=15) as r:
            return json.loads(r.read())
    except Exception as e:
        return {"error": str(e)}


def _get(path: str) -> dict:
    try:
        with urllib.request.urlopen(BASE_URL + path, timeout=8) as r:
            return json.loads(r.read())
    except Exception as e:
        return {"error": str(e)}


def _llm_generate(prompt: str, max_tokens: int = 400) -> Optional[str]:
    result = _post("/api/llm/generate", {"prompt": prompt, "max_tokens": max_tokens, "temperature": 0.5})
    if "error" not in result and result.get("text"):
        return result["text"].strip()
    return None


def _man_template(cmd: str, synopsis: str, description: str, options: list[str], examples: list[str]) -> str:
    opt_block = "\n".join(f"  {o}" for o in options) if options else "  (none)"
    ex_block = "\n".join(f"  {e}" for e in examples) if examples else "  (none)"
    return f"""# {cmd.upper()}(1) — Terminal Depths Man Page

## NAME
  {cmd} — {synopsis}

## SYNOPSIS
  {cmd} [OPTIONS] [ARGS]

## DESCRIPTION
{description}

## OPTIONS
{opt_block}

## EXAMPLES
{ex_block}

## ENVIRONMENT
  Terminal Depths v1.0 — cyberpunk hacking simulation

## SEE ALSO
  help, man, commands

---
*Generated by agents/documenter.py on {time.strftime("%Y-%m-%d")}*
"""


def _lore_template(agent_id: str, name: str, faction: str, role: str, lore: str) -> str:
    return f"""# {name} — Agent Dossier

**ID:** `{agent_id}`
**Faction:** {faction}
**Role:** {role}

## Background
{lore}

## Known Operations
*[CLASSIFIED — clearance level 4 required]*

## Communication Protocol
Contact via terminal command: `agents` → select {name}

---
*Dossier compiled: {time.strftime("%Y-%m-%d")} | Classification: RESTRICTED*
"""


